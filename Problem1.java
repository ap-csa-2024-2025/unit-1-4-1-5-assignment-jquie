public class Problem1
{
    public static void main(String[] args)
   {
    int value = 24;
    System.out.println(value);

    for (int i = 0; i < 4; i++) {
        value++;
        System.out.println(value);
    }

    for (int i = 0; i < 4; i++) {
        value--;
        System.out.println(value);
    }
   } 
}
